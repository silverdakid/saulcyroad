bool makeChoice(char *question){
    char reponse;
    printf("%s",question);

    while (1) { 
        scanf(" %c", &reponse);  
        if (reponse == 'y' || reponse == 'Y') {
            return true;
        } else if (reponse == 'n' || reponse == 'N') {
           return false;
        } else {
            printf("Entrée invalide, veuillez entrer 'y' ou 'n'.\n\n Votre réponse :");
        }
    }

    return false;
}

